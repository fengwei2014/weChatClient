package slp.tt.ui.model;

public class WeChatConfig {
	private String projectName;
	private String vehicleInfoUrl;
	private String historyTrackUrl;
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getVehicleInfoUrl() {
		return vehicleInfoUrl;
	}
	public void setVehicleInfoUrl(String vehicleInfoUrl) {
		this.vehicleInfoUrl = vehicleInfoUrl;
	}
	public String getHistoryTrackUrl() {
		return historyTrackUrl;
	}
	public void setHistoryTrackUrl(String historyTrackUrl) {
		this.historyTrackUrl = historyTrackUrl;
	}
	

}
